package com.kas.q.ext;

public enum MessageClass
{
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  cMessage         (0x01),
  cTextMessage     (0x02),
  cObjectMessage   (0x04),
  cBytesMessage    (0x08),
  cStreamMessage   (0x10),
  cMapMessage      (0x20),
  //
  cJavaxJmsMessage (0x80)
  ;
  
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  private final static MessageClass [] cValues = MessageClass.values();
  
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  private int mMessageClass;
  
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  private MessageClass(int messageClass)
  {
    mMessageClass = messageClass;
  }
  
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  private int getMessageClass()
  {
    return mMessageClass;
  }
  
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  public boolean isInstanceOf(MessageClass messageClass)
  {
    return ((mMessageClass >= messageClass.getMessageClass()) && (mMessageClass & messageClass.getMessageClass()) != 0);
  }
  
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  public static MessageClass fromInt(int ordinal)
  {
    if ((ordinal >= cValues.length) || (ordinal < 0))
      throw new ArrayIndexOutOfBoundsException("Unknown ordinal value " + ordinal + " for enum " + MessageClass.class.getName());
    
    return cValues[ordinal];
  }
}
