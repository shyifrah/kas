package com.kas.logging.impl;

public enum LogLevel
{
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  FATAL,
  ERROR,
  WARN,
  INFO,
  TRACE,
  DEBUG,
  DIAG;
  
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  private String mName = null;
  
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  public boolean isGreaterOrEqual(LogLevel other)
  {
    return this.compareTo(other) >= 0;
  }
  
  //------------------------------------------------------------------------------------------------------------------
  //
  //------------------------------------------------------------------------------------------------------------------
  public String toString()
  {
    if (mName == null)
    {
      String str = name() + "     ";
      mName = str.substring(0,5);
    }
    return mName;
  }
}
